Facade Design Pattern:
    Defination:
        - The facade pattern is a structural design pattern that provides
         a simplified interface to a set of complex subsystem, making it 
         easier to use while minimizing client dependencies.
    
    Concept:
        - Acts as a "mask" to simplify complex operations.
        - Encapsulates multiple intricate methods into simplified, 
        user friendly methods.

    Purpose:
        - Makes complex systems easier to interact with
        - Reduces the difficulity and effort for the client.
        - Minimizes dependency on the underlying subsystem.
    
    Analogy:
        - Similar to a "popcorn button" on a microwave. 
        Instead of manually setting time, power, and starting the microwave, 
        the button automates the process with pre-configured settings.

    Benefits:
        - Imporves usability
        - Reduces coupling between the client and the subsystem.
        - Promotes a cleaner and more managable code structure.
        