State:
    - A behavioral design pattern that allows an object to alter 
    its behavior when its state changes.
    
    - It uses inheritance and polymorphism to allow subclasses to 
    represent the different states and functionality that can change
    during runtime.
    
    - This is an easy way for an object to change its class type while the
    program is running.

    - State pattern is commonly used to represent the different states
    of a state machine.

